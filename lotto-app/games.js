export function dreamFallback(text) {
  const t = String(text || '');
  const matched = [];
  const matchedKws = [];
  for (const e of DREAMBOOK) {
    const kw = e.kw.find((k) => t.includes(k));
    if (!kw) continue;
    // skip if a more specific symbol already covered this family (e.g. งูใหญ่ vs งู)
    if (matchedKws.some((p) => p.includes(kw) || kw.includes(p))) continue;
    matched.push(e);
    matchedKws.push(kw);
  }
  if (matched.length) {
    const parts = matched.map((m) => m.say);
    for (const mod of DREAM_MODS) if (mod.kw.some((k) => t.includes(k))) parts.push(mod.add);
    const numbers = [...new Set(matched.flatMap((m) => m.nums))].slice(0, 4);
    return { numbers, interpretation: parts.join(' '), source: 'ตำราฝันไทย' };
  }
  // no symbol matched: derive numbers from the text, generic interpretation
  let sum = 0; for (const ch of t) sum += ch.charCodeAt(0);
  const two = String(sum % 100).padStart(2, '0');
  const three = String(sum % 1000).padStart(3, '0');
  return {
    numbers: [two, three],
    interpretation: 'ความฝันของคุณไม่ตรงกับสัญลักษณ์ในตำราฝันที่เรามี แต่ทุกความฝันล้วนมีความหมาย เราจึงถอดเป็นเลขนำโชคจากพลังของความฝันนี้ให้คุณ ลองเก็บไว้เป็นเลขเสี่ยงทายเพื่อความบันเทิงดูนะ',
    source: 'สุ่มจากความฝัน',
  };
}

// ---- personal lucky numbers (เลขศาสตร์) ---------------------------------
function reduceTo2(n) {
  let x = Math.abs(parseInt(n, 10) || 0);
  while (x >= 100) x = String(x).split('').reduce((s, d) => s + +d, 0);
  return String(x).padStart(2, '0');
}
// from a birthdate yyyy-mm-dd -> a few lucky numbers
export function luckyFromBirthdate(iso) {
  const m = String(iso || '').match(/(\d{4})-(\d{2})-(\d{2})/);
  if (!m) return null;
  const [, y, mo, d] = m;
  const dd = +d, mm = +mo, yy = +y;
  const digitsum = (n) => String(n).split('').reduce((s, x) => s + +x, 0);
  const core = reduceTo2(dd + mm + digitsum(yy));
  const a = String(dd).padStart(2, '0');
  const b = reduceTo2(dd * mm);
  const three = String((dd * 100 + mm * 7 + digitsum(yy)) % 1000).padStart(3, '0');
  return { numbers: [core, a, b, three].filter((v, i, arr) => arr.indexOf(v) === i) };
}
